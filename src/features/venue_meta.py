def bank_length(venue_code: str) -> int | None:
    """venue_code のバンク周長(m)を返す。不明コードは None。"""
    v = VENUE.get(venue_code)
    return v["bank"] if v else None


def venue_name(venue_code: str) -> str | None:
    """venue_code の会場名を返す。不明コードは None。"""
    v = VENUE.get(venue_code)
    return v["name"] if v else None
